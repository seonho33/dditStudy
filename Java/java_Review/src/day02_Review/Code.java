/*2️⃣ 코드 작성 & 완성

기능	단축키	설명
코드 자동 완성	Ctrl + Space	변수, 메소드, 클래스 자동 추천
메소드/변수 선언으로 이동	F3	선언부로 이동
사용처 검색	Ctrl + Shift + G	해당 변수/메소드 사용 위치 전체 검색
빠른 수정	Ctrl + 1	경고/오류 해결, import 자동 추가 등
제안 목록 보기	Ctrl + Shift + Space	메소드 매개변수 자동 추천

3️⃣ 편리한 탐색
기능	단축키	설명
파일/클래스 열기	Ctrl + Shift + R	프로젝트 내 파일 검색 후 바로 열기
타입 검색	Ctrl + Shift + T	클래스, 인터페이스, enum 등 타입 검색
최근 편집 위치 이동	Ctrl + Q	마지막 커서 위치로 이동
다음/이전 문제로 이동	Ctrl + . / Ctrl + ,	오류/경고 위치 이동
빠른 열기	Ctrl + E	열린 편집기 사이 이동

4️⃣ 디버깅 & 실행
기능	단축키	설명
프로그램 실행	Ctrl + F11	마지막 실행/디버그 실행
디버깅 시작	F11	선택된 클래스/프로젝트 디버그
브레이크포인트 토글	Ctrl + Shift + B	현재 줄 브레이크포인트 설정/해제
한 단계씩 실행	F5 / F6 / F7 / F8	디버깅 중 단계별 코드 실행

5️⃣ 유용한 팁
Shift + Alt + F → 소스 포맷팅 (코드 자동 정렬)
Ctrl + O → 현재 클래스 멤버 목록 보기
Ctrl + Shift + F → 선택 영역 코드 포맷팅

💡 정리하면,
Shift + Enter → 줄 내 새 줄
Ctrl + Alt + ↑/↓ → 줄 복사
Alt + ↑/↓ → 줄 이동
Ctrl + / → 주석 토글
Ctrl + 1 → 경고/오류 빠른 수정
이 5개만 익혀도 코드 작성 속도가 확실히 빨라집니다. */